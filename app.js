// –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –ø–æ–∏—Å–∫–∞
function setupSearch() {
    const searchInput = document.getElementById('searchInput');
    const searchResults = document.getElementById('searchResults');
    
    searchInput.addEventListener('input', function() {
        const query = this.value.toLowerCase().trim();
        
        if (query.length < 2) {
            searchResults.style.display = 'none';
            return;
        }
        
        const results = searchContent(query);
        displaySearchResults(results);
    });
}

// –ü–æ–∏—Å–∫ –ø–æ —Å–æ–¥–µ—Ä–∂–∏–º–æ–º—É
function searchContent(query) {
    const results = [];
    
    // –ü–æ–∏—Å–∫ –ø–æ –≥–ª–∞–≤–∞–º –∏ –ø–æ–¥–≥–ª–∞–≤–∞–º
    warehouseData.chapters.forEach(chapter => {
        if (chapter.title.toLowerCase().includes(query) || 
            chapter.description.toLowerCase().includes(query)) {
            results.push({
                type: 'chapter',
                title: chapter.title,
                description: chapter.description,
                id: chapter.id
            });
        }
        
        chapter.subchapters.forEach(sub => {
            if (sub.title.toLowerCase().includes(query)) {
                results.push({
                    type: 'subchapter',
                    title: `${sub.id} ${sub.title}`,
                    description: `–†–∞–∑–¥–µ–ª: ${chapter.title}`,
                    id: sub.id
                });
            }
        });
    });
    
    // –ü–æ–∏—Å–∫ –ø–æ —Å–æ–¥–µ—Ä–∂–∏–º–æ–º—É
    Object.keys(warehouseData.content).forEach(key => {
        const content = warehouseData.content[key];
        if (content.title.toLowerCase().includes(query) || 
            content.content.toLowerCase().includes(query)) {
            results.push({
                type: 'content',
                title: content.title,
                description: '–°–æ–¥–µ—Ä–∂–∏–º–æ–µ —Ä–∞–∑–¥–µ–ª–∞',
                id: key
            });
        }
    });
    
    return results.slice(0, 10); // –û–≥—Ä–∞–Ω–∏—á–∏–≤–∞–µ–º 10 —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞–º–∏
}

// –û—Ç–æ–±—Ä–∞–∂–µ–Ω–∏–µ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤ –ø–æ–∏—Å–∫–∞
function displaySearchResults(results) {
    const searchResults = document.getElementById('searchResults');
    
    if (results.length === 0) {
        searchResults.innerHTML = '<div class="search-result"><p>–ù–∏—á–µ–≥–æ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ</p></div>';
    } else {
        searchResults.innerHTML = results.map(result => `
            <div class="search-result" onclick="handleSearchResult('${result.id}', '${result.type}')">
                <h3>${result.title}</h3>
                <p>${result.description}</p>
            </div>
        `).join('');
    }
    
    searchResults.style.display = 'block';
}

